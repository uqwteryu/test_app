class Case {
  final String name; // Name of the case
  final List<Map<String, dynamic>> items; // List of items in the case

  Case(this.name, this.items); // Constructor to initialize name and items
}

// Represents a case with a name and a list of items.
// - `final String name`: Holds the name of the case.
// - `final List<Map<String, dynamic>> items`: Contains the list of items in the case. Each item is represented by a map with various properties.
// - `Case(this.name, this.items)`: Constructor to instantiate a case object with a given name and list of items.
